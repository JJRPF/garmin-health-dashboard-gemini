#!/usr/bin/env node
/**
 * Run once to generate VAPID keys for Web Push notifications.
 * Adds them directly to .env.local so you don't have to copy-paste.
 *
 * Usage:
 *   node scripts/generate-vapid-keys.js
 *
 * Then add these same variables to Vercel:
 *   vercel env add NEXT_PUBLIC_VAPID_PUBLIC_KEY
 *   vercel env add VAPID_PRIVATE_KEY
 *   vercel env add VAPID_EMAIL
 */
const webpush = require('web-push');
const fs = require('fs');
const path = require('path');

const keys = webpush.generateVAPIDKeys();
const envPath = path.join(__dirname, '..', '.env.local');

// Read existing .env.local if it exists
let existing = '';
if (fs.existsSync(envPath)) {
  existing = fs.readFileSync(envPath, 'utf8');
}

// Remove any existing VAPID lines to avoid duplicates
const filtered = existing
  .split('\n')
  .filter(l => !l.startsWith('NEXT_PUBLIC_VAPID_PUBLIC_KEY') && !l.startsWith('VAPID_PRIVATE_KEY') && !l.startsWith('VAPID_EMAIL'))
  .join('\n')
  .trimEnd();

const newLines = [
  '',
  '# Web Push VAPID (generated by scripts/generate-vapid-keys.js — do NOT commit)',
  `NEXT_PUBLIC_VAPID_PUBLIC_KEY=${keys.publicKey}`,
  `VAPID_PRIVATE_KEY=${keys.privateKey}`,
  `VAPID_EMAIL=mailto:tu@email.com`,
  '',
].join('\n');

fs.writeFileSync(envPath, filtered + newLines);

console.log('\n✅  VAPID keys generated and written to .env.local\n');
console.log('  Public  (safe to expose):', keys.publicKey);
console.log('  Private (keep secret!)  :', keys.privateKey);
console.log('\n📌  Next steps:');
console.log('  1. Edit VAPID_EMAIL in .env.local with your real email');
console.log('  2. Add the same variables to Vercel:');
console.log('       vercel env add NEXT_PUBLIC_VAPID_PUBLIC_KEY production');
console.log('       vercel env add VAPID_PRIVATE_KEY production');
console.log('       vercel env add VAPID_EMAIL production');
console.log('  3. Optionally set BATTERY_THRESHOLD (default: 30)\n');
